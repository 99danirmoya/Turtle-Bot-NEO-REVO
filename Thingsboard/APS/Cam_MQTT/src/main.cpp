void reconnectMQTT() {
    while (!client.connected()) {
        Serial.print("Connecting to ThingsBoard...");
        if (client.connect("ESP32CAM", access_token, NULL)) {
            Serial.println("connected");
        } else {
            Serial.print("failed, rc=");
            Serial.print(client.state());
            Serial.println(" retrying in 5 seconds");
            delay(5000);
        }
    }
}

void captureAndPublish() {
    camera_fb_t* fb = esp_camera_fb_get();
    if (!fb) {
        Serial.println("Camera capture failed");
       // return;
    }

    Serial.printf("Picture taken! Size: %zu bytes\n", fb->len);

    // Convert to base64
    String base64Image = base64::encode(fb->buf, fb->len);
    Serial.printf("Base64 size: %d bytes\n", base64Image.length());

    // Create JSON document
    StaticJsonDocument<52768> doc; // Adjust size based on your needs
    doc["ts"] = millis(); // Timestamp
    doc["image"] = base64Image;

    // Serialize JSON to string
    String jsonString;
    serializeJson(doc, jsonString);

    // Publish to ThingsBoard
    if (client.publish(mqtt_topic.c_str(), jsonString.c_str())) {
        Serial.println("Photo published successfully to ThingsBoard!");
    } else {
        Serial.println("Failed to publish photo");
        Serial.printf("MQTT state: %d\n", client.state());
    }

    esp_camera_fb_return(fb);
}

void setup() {
    Serial.begin(115200);
    WRITE_PERI_REG(RTC_CNTL_BROWN_OUT_REG, 0);
    
    setupCamera();
    connectToWiFi();
    setupSecureClient();
    
    client.setServer(mqtt_server, mqtt_port);
    client.setBufferSize(52768); // Increase buffer size
}

void loop() {
    if (!client.connected()) {
        reconnectMQTT();
    }
    client.loop();
    
    captureAndPublish();
    delay(2000);  // Wait 10 seconds between captures
}